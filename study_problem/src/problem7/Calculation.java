package problem7;

public class Calculation {

	private int Num1;

	private int Num2;

	public int getNum1() {
		return Num1;
	}

	public void setNum1(int num1) {
		Num1 = num1;
	}

	public int getNum2() {
		return Num2;
	}

	public void setNum2(int num2) {
		Num2 = num2;
	}

	public int add() {
		return Num1+Num2;
	}

	public int sub() {
		return Num1-Num2;
	}

}
